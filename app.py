import streamlit as st
import pickle
import re
import string

# Load model
with open("/content/spam_classifier.pkl", "rb") as f:
    model = pickle.load(f)

# Preprocessing function
def preprocess(text):
    text = text.lower()
    text = re.sub(r'\d+', '', text)
    text = text.translate(str.maketrans('', '', string.punctuation))
    return text

# UI
st.title("ðŸ“¬ Spam vs. Ham Email Classifier")
st.write("Enter an email message below. The model will predict whether it is spam or ham.")

input_text = st.text_area("âœ‰ Email Content")

if st.button("Classify"):
    if not input_text.strip():
        st.warning("Please enter some text.")
    else:
        cleaned = preprocess(input_text)
        prediction = model.predict([cleaned])[0]
        if prediction == "spam":
            st.error("ðŸš¨ This is SPAM!")
        else:
            st.success("âœ… This is HAMÂ (notÂ spam).")